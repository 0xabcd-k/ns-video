export function getText(item){
    const userLang = navigator.language;
    let ans = null
    if(item){
        try{ ans = item[userLang]}catch (e){}
    }else{}
    if(!ans){
        try{
            return item["en-US"]
        }catch (e){
            return "***"
        }
    }else{
        return ans
    }
}